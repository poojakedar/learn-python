"""Day 1: Variables, data types, and print()."""

print("=== Day 1: Variables and Data Types ===")

# String
name = "Pooja"

# Integer
age = 20

# Float
height = 5.4

# Boolean
is_student = True

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is student:", is_student)

print("\nType checks:")
print(type(name))
print(type(age))
print(type(height))
print(type(is_student))

# Reassigning a variable
age = 21
print("\nUpdated age:", age)

